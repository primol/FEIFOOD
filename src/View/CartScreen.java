    private void btnFinalizeActionPerformed(java.awt.event.ActionEvent evt) {                                            
        // Here you would normally save the order to the database
        // For now, just show a message and open the order history
        JOptionPane.showMessageDialog(this, "Pedido finalizado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        CartManager.getInstance().clearCart();
        OrderHistory orderHistory = new OrderHistory();
        new OrderHistoryController(orderHistory, UserSession.getInstance().getLoggedInAluno().getId());
        orderHistory.setVisible(true);
        this.dispose();
    }                                           

    private void loadCart() {
        List<CartItem> items = CartManager.getInstance().getCartItems();
        DefaultListModel<String> model = new DefaultListModel<>();
        for (CartItem item : items) {
            model.addElement(item.getFood().getName() + " - Quantidade: " + item.getQuantity());
        }
        lstCart.setModel(model);
        updateTotal();
    }

    private void updateTotal() {
        double total = CartManager.getInstance().getTotal();
        lblTotal.setText("Total: R$ " + String.format("%.2f", total));
    }

